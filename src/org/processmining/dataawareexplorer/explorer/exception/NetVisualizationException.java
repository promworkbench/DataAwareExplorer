package org.processmining.dataawareexplorer.explorer.exception;

public class NetVisualizationException extends Exception {

	private static final long serialVersionUID = -3260476090162886938L;

	public NetVisualizationException() {
		super();
	}

	public NetVisualizationException(String message, Throwable cause) {
		super(message, cause);
	}

	public NetVisualizationException(String message) {
		super(message);
	}

	public NetVisualizationException(Throwable cause) {
		super(cause);
	}

}
