package org.processmining.dataawareexplorer.explorer.exception;


public class FilterException extends NetVisualizationException {

	private static final long serialVersionUID = 3308405667234985762L;

	public FilterException() {
		super();
	}

	public FilterException(String message, Throwable cause) {
		super(message, cause);
	}

	public FilterException(String message) {
		super(message);
	}

	public FilterException(Throwable cause) {
		super(cause);
	}
	
}
